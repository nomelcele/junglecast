package or.junglecast.vo;

public class BestArticleVO {
	private String article_title, recommend_date;
	private int article_id, count;
	
	
	
	public String getRecommend_date() {
		return recommend_date;
	}
	public void setRecommend_date(String recommend_date) {
		this.recommend_date = recommend_date;
	}
	public String getArticle_title() {
		return article_title;
	}
	public void setArticle_title(String article_title) {
		this.article_title = article_title;
	}
	public int getArticle_id() {
		return article_id;
	}
	public void setArticle_id(int article_id) {
		this.article_id = article_id;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	
}
